--3. REVENUE GROWTH RATE--
SELECT
  CLASS_TYPE.CLASS_TYPE AS CLASS,
  BOOKING_OFFICE.OFFICE_ID,
  SUM(CAST(FARE.FINAL_PRICE AS DECIMAL(10, 2))) AS REVENUE
FROM TICKET
JOIN BOOKING_OFFICE ON TICKET.OFFICE_ID = BOOKING_OFFICE.OFFICE_ID
JOIN CLASS_TYPE ON BOOKING_OFFICE.OFFICE_ID = CLASS_TYPE.OFFICE_ID
JOIN FARE ON CLASS_TYPE.C_TYPE_ID = FARE.C_TYPE_ID
GROUP BY CLASS_TYPE.CLASS_TYPE, BOOKING_OFFICE.OFFICE_ID
ORDER BY CLASS_TYPE.CLASS_TYPE, BOOKING_OFFICE.OFFICE_ID;

