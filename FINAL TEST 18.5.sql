USE FINALTEST;

CREATE TABLE AIRPLANE(
REGNO VARCHAR(225) NOT NULL,
A_TYPE VARCHAR(225) NOT NULL,
TOTAL_SEAT INT,
CONSTRAINT REGNO_PK PRIMARY KEY (REGNO));

CREATE TABLE FLIGHT(
FLIGHTNO VARCHAR(225),
REGNO VARCHAR(225),
F_ROUTE VARCHAR(225),
DEPT_TIME TIME,
ARR_TIME TIME,
DEPT_DATE DATE,
ARR_DATE DATE,
CONSTRAINT FLIGHTNO_PK PRIMARY KEY (FLIGHTNO),
CONSTRAINT REGNO_FK FOREIGN KEY (REGNO) REFERENCES AIRPLANE(REGNO));

CREATE TABLE AIRPORT(
A_ID VARCHAR(225) NOT NULL,
COUNTRY VARCHAR(225),
CITY VARCHAR(225),
FLIGHTNO VARCHAR(225),
CONSTRAINT FLIGHTNO_FK FOREIGN KEY (FLIGHTNO) REFERENCES FLIGHT(FLIGHTNO));

CREATE TABLE CABIN_CREW(
C_ID VARCHAR(225) NOT NULL,
CAPTAIN_NAME VARCHAR(225) NOT NULL,
FLIGHTNO VARCHAR(225),
CONSTRAINT FLIGHT_NO_FK FOREIGN KEY (FLIGHTNO) REFERENCES FLIGHT(FLIGHTNO));

CREATE TABLE BOOKING_OFFICE(
OFFICE_ID VARCHAR(225),
OFFICE_NAME VARCHAR(225),
FLIGHTNO VARCHAR(225),
CONSTRAINT OFFICEID_PK PRIMARY KEY (OFFICE_ID),
CONSTRAINT FLIGHT_NOO_FK FOREIGN KEY (FLIGHTNO) REFERENCES FLIGHT(FLIGHTNO));


CREATE TABLE TICKET(
TICKETNO VARCHAR(225) NOT NULL,
CLASS_TYPE VARCHAR(225) CHECK(CLASS_TYPE IN ('ECONOMY CLASS', 'BUSINESS CLASS','PRIVATE CLASS')),
SEATNO VARCHAR(225) NOT NULL,
OFFICE_ID VARCHAR(225),
PAYMENT_DATE DATE,
PAYMENT_TIME TIME,
CONSTRAINT TICKETNO_PK PRIMARY KEY (TICKETNO),
CONSTRAINT OFFICEID_FK FOREIGN KEY (OFFICE_ID) REFERENCES BOOKING_OFFICE(OFFICE_ID));


CREATE TABLE CLASS_TYPE(
OFFICE_ID VARCHAR(225),
FLIGHTNO VARCHAR(225),
CLASS_TYPE VARCHAR(225) CHECK(CLASS_TYPE IN ('ECONOMY CLASS', 'BUSINESS CLASS','PRIVATE CLASS')),
C_TYPE_ID VARCHAR(225),
CONSTRAINT CTYPEID_PK PRIMARY KEY (C_TYPE_ID),
CONSTRAINT OFFICE_ID_FK FOREIGN KEY (OFFICE_ID) REFERENCES BOOKING_OFFICE(OFFICE_ID));


CREATE TABLE FARE(
C_TYPE_ID VARCHAR(225),
COST VARCHAR(225),
OPERATION_COST VARCHAR(225),
FINAL_PRICE VARCHAR(225),
CONSTRAINT CTYPEID_FK FOREIGN KEY (C_TYPE_ID) REFERENCES CLASS_TYPE(C_TYPE_ID));


CREATE TABLE PASSENGER_A(
P_ID_A INT NOT NULL,
P_NAME VARCHAR(225) NOT NULL,
AGE INT NOT NULL,
GENDER VARCHAR(225) CHECK(GENDER IN ('MALE', 'FEMALE')),
CONTACT_NUM VARCHAR(225) NOT NULL,
EMAIL VARCHAR(225) NOT NULL,
CITY VARCHAR(225) NOT NULL,
TICKETNO VARCHAR(225) NOT NULL,
CONSTRAINT P_ID_A_PK PRIMARY KEY (P_ID_A),
CONSTRAINT TICKETNO_FK FOREIGN KEY (TICKETNO) REFERENCES TICKET(TICKETNO));

CREATE TABLE PASSENGER_C(
P_ID INT NOT NULL,
P_NAME VARCHAR(225) NOT NULL,
AGE INT NOT NULL,
GENDER VARCHAR(225) CHECK(GENDER IN ('MALE', 'FEMALE')),
P_ID_A INT NOT NULL,
CITY VARCHAR(225) NOT NULL,
TICKETNO VARCHAR(225) NOT NULL,
CONSTRAINT TICKET_NO_FK FOREIGN KEY (TICKETNO) REFERENCES TICKET(TICKETNO),
CONSTRAINT P_ID_A_FK FOREIGN KEY (P_ID_A) REFERENCES PASSENGER_A(P_ID_A));

CREATE TABLE TOTAL_PASSENGER(
P_ID INT NOT NULL,
P_NAME VARCHAR(225) NOT NULL,
AGE INT NOT NULL,
GENDER VARCHAR(225) CHECK(GENDER IN ('MALE', 'FEMALE')),
CITY VARCHAR(225) NOT NULL,
TICKETNO VARCHAR(225) NOT NULL,
CONSTRAINT TICKET_N_O_FK FOREIGN KEY (TICKETNO) REFERENCES TICKET(TICKETNO));


